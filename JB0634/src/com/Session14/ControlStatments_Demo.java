package com.Session14;

/**
 * @author Tejkumar Objective: Introduction to Control Statements in Java
 *
 */
public class ControlStatments_Demo {
	public static void main(String[] args) {

		int a = 10;
		int b = 20;

		// Every Statements in java should be terminated by a ;(Semicolon)

		if (a > b) 
		{
			// yes path or true path
			System.out.println("A is greater");
		} 
		else
		{
			// no path or wrong path false path
			System.out.println("B is greater");
		}

	}
}


