//Owner: Tejkumar, Objective: This Java Program illustrates the concept of if else (Conditional Blocks) in java
public class Test1 
{
	//main method
	public static void main(String[] args) 
	{
		//Declare 2 variables as a, b. Compare them and print the largest among them. 
				
		//Declare a 
		int a = 10;
		
		//Declare b
		int b = 20;
		
		//Compare a and b
		if (a > b) 
		{
			System.out.println("A is greater");
		} 
		else 
		{
			System.out.println("B is greater");
		}

	}

}
